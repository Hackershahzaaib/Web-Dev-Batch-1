// Step 1: Declare and assign variables
const var1 = 10;
let var2 = "Hello";
var var3 = true;

// Log initial values to the console
console.log("Initial values:");
console.log("var1:", var1);
console.log("var2:", var2);
console.log("var3:", var3);

// Step 2: Modify the variables
// var1 = var1 + 5; // This will throw an error because var1 is a constant
var2 = var2 + " World!";   // var2 becomes "Hello World!"
var3=false;              // var3 becomes false

// Log modified values to the console
console.log("Modified values:");
console.log("var2:", var2);
console.log("var3:", var3);



